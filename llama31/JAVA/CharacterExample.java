import java.lang.String;

public class CharacterExample {
    char a = 'A'; // single character
    String str = "Hello World"; // string of characters

    public static void main(String[] args) {
        CharacterExample example = new CharacterExample();
        System.out.println("Char: " + example.a);
        System.out.println("String: " + example.str);
    }
}