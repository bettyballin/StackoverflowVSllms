import java.lang.String;
// Example usage of Randoop in a Java project
public class MyClass_36 {
    public int add(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        // Normally, this would be run via command line with Randoop, not inside the code.
        // This is just an illustrative example of what Randoop generates.
        MyClass_36 myClass = new MyClass_36();
        int result = myClass.add(5, 10);
        System.out.println("Result: " + result); // Expected output: Result: 15
    }
}